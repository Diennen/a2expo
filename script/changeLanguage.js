const headerLanguages = document.querySelector('.header-languages-menu');
const activeLanguage = document.querySelector('.header-active-language');
const languageButtons = headerLanguages.querySelectorAll('.header-languages-element-option');
const allLang = ['en', 'pl', 'fr'];
const defLang = 'en';
let selectedLanguage = localStorage.getItem('selectedLng');
let choosedLanguageAfter = checkChoosedLanguage();

localStorage.setItem('selectedLng', choosedLanguageAfter);

allLanguageFunctions();

// --------------------------------------------------
for (let i = 0; i < languageButtons.length; i++) {
	languageButtons[i].addEventListener('click', allLanguageFunctions);
}

function allLanguageFunctions(event) {
	changeURLLanguage(event);
	changeActiveLanguage(event);
	// changeHeadLanguageOptions(event);
	changeLanguage(event);
	reloadPageIFClick(event);
}

// Проверка языка из хранилища
function checkChoosedLanguage() {
	let choosedLanguage;

	for (let i = 0; i < allLang.length; i++) {
		if (selectedLanguage === allLang[i]) {
			choosedLanguage = allLang[i];
			break;
		}
		else {
			choosedLanguage = defLang;
		}
	}
	return choosedLanguage;
}

// Показ выбранного языка в меню
function changeActiveLanguage(event) {
	if (event) {
		const choosedLanguage = event.currentTarget;
		const choosedLanguageHTML = choosedLanguage.outerHTML;
		activeLanguage.innerHTML = choosedLanguageHTML;
	}
	else {
		const attrToSearch = choosedLanguageAfter;
		const attr = `[data-lang-value="${attrToSearch}"]`
		const lang = document.querySelector(attr);
		const langHTML = lang.outerHTML;
		activeLanguage.innerHTML = langHTML;
	}
}

// Добавление языка в URL
function changeURLLanguage(event) {
	if (event) {
		const pressedButton = event.currentTarget;
		const attr = pressedButton.getAttribute('data-lang-value')
		let lang = attr;
		location.href = window.location.pathname + "#" + lang;
	}
	else {
		location.href = window.location.pathname + "#" + choosedLanguageAfter;
	}
}

// Смена языка
function changeLanguage(event) {
	const htmlElement = document.querySelector('html');
	const headDescription = document.querySelector('meta[name="description"]');
	const headKeyWords = document.querySelector('meta[name="keywords"]');

	let hash = window.location.hash;
	hash = hash.substr(1);

	// Проверка на доступные языки
	if (!allLang.includes(hash)) {
		// #en - язык по умолчанию
		location.href = window.location.pathname + '#' + choosedLanguageAfter;
		location.reload();
	}


	// Запись выбранного языка в LocalStorage
	localStorage.setItem('selectedLng', hash);

	// Смена у head
	if (hash === "en") {
		htmlElement.setAttribute('lang', 'en');
		headDescription.setAttribute('content', 'Creating layouts, stands, assembling advertisements');
		headKeyWords.setAttribute('content', 'a2expo, expo, creation, layouts, stands, advertising assembly, web design, online advertising, advertising graphics, ad creation, advertising services, trade show stands, ad assembly, advertising campaigns');
		document.title = "A2EXPO - Creating layouts, stands, assembling advertisements";
	}

	else if (hash === "pl") {
		htmlElement.setAttribute('lang', 'pl');
		headDescription.setAttribute('content', 'Tworzenie układów, stoisk, montaż reklam');
		headKeyWords.setAttribute('content', 'a2expo, expo, tworzenie, layouty, standy, montaż reklam, projektowanie stron, reklama online, grafika reklamowa, kreacja reklam, usługi reklamowe, standy targowe, składanie reklam, kampanie reklamowe');
		document.title = "A2EXPO - Tworzenie układów, stoisk, montaż reklam";
	}

	else if (hash === "fr") {
		htmlElement.setAttribute('lang', 'fr');
		headDescription.setAttribute('content', 'Création de mises en page, de stands, montage de publicités');
		headKeyWords.setAttribute('content', 'a2expo, expo, création, mises en page, stands, montage publicitaire, conception de sites web, publicité en ligne, graphisme publicitaire, création publicitaire, services publicitaires, montage publicitaire, campagnes publicitaires');
		document.title = "A2EXPO - Création de mises en page, de stands, montage de publicités";
	}

	else {
		htmlElement.setAttribute('lang', 'en');
		headDescription.setAttribute('content', 'Creating layouts, stands, assembling advertisements');
		headKeyWords.setAttribute('content', 'creation, layouts, stands, advertising assembly, web design, online advertising, advertising graphics, ad creation, advertising services, trade show stands, ad assembly, advertising campaigns');
		document.title = "A2EXPO - Creating layouts, stands, assembling advertisements";
	}

	for (let key in langArr) {
		let elem = document.querySelector('.lng-' + key);
		// Проверка на наличие переводимого элемента, потом смена языка
		if (elem) {
			elem.innerHTML = langArr[key][hash];
		}
	}
}

